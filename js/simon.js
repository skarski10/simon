var colorSelect = [];
function simon(){
  var result = Math.floor((Math.random() * 4) + 1);
  if (result = 1) {
    colorSelect.push("red");
  }
  if (result = 2) {
    colorSelect.push("blue");
  }
  if (result = 3) {
    colorSelect.push("green");
  }
  if (result = 4) {
    colorSelect.push("yellow");
  }
}
